int		ft_countbits(int n)
{
	int	r;
	int	i;
	int	max;

	i = 0;
	r = 0;
	max = (sizeof(int) * 8);
	while (i < max)
	{
		if (n & 1)
			r = i + 1;
		n = (n >> 1);
		i++;
	}
	return (r);
}
